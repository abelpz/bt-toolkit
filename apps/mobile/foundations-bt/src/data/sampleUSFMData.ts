// Helper function to get USFM data for a book
export const getUSFMForBook = (bookCode: string): string => {
  // Handle both 'Jonah' and 'JONAH' formats
  const bookMap: Record<string, string> = {
    'Jonah': 'JONAH',
    'Philemon': 'PHILEMON',
    'Romans': 'ROMANS',
    'Titus': 'TITUS',
  };
  
  const mappedCode = bookMap[bookCode] || bookCode.toUpperCase();
  return sampleUSFMData[mappedCode as keyof typeof sampleUSFMData] || '';
};

// Helper function to get translation notes for a book
export const getNotesForBook = (bookCode: string) => {
  const normalizedCode = bookCode.toUpperCase();
  return sampleTranslationNotes[normalizedCode as keyof typeof sampleTranslationNotes] || [];
};

// Helper function to get notes for a specific reference
export const getNotesForReference = (bookCode: string, reference: string) => {
  const allNotes = getNotesForBook(bookCode);
  return allNotes.filter(note => note.reference === reference);
};

